
public class SwitchCases2 {

	public static void main(String[] args) {
		
		int age = 3;
	switch (age) {
	
	case 1:
		System.out.println("You might've said your first"
				+ " word but you cannot walk");
		break;
	case 2: 
		System.out.println("You can somewhat"
				+ " talk and walk!");
		break;
	case 3: 
		System.out.println("You can talk real well,"
				+ " and you can write and doodle!");
		break;
	case 4: 
		System.out.println("You go to school next year"
				+ " and you're socially active!");
		break;
		default:
			System.out.println("I don't how old you are!");
		break;
	
	
	}
		

	}

}
